<?php

namespace App\Repository;

use App\Entity\Message;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Message>
 *
 * @method Message|null find($id, $lockMode = null, $lockVersion = null)
 * @method Message|null findOneBy(array $criteria, array $orderBy = null)
 * @method Message[]    findAll()
 * @method Message[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class MessageRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Message::class);
    }

    public function save(Message $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Message $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }
    public function getByUsers(?int $sender_id, ?int $recipent_id){
        $queryBuilder = $this->getEntityManager()->createQueryBuilder();
        $queryBuilder->select('m')
                     ->from(Message::class, 'm')
                     ->where('m.sender = :sender_id and m.recipent = :recipent_id')
                     ->orWhere('m.sender = :recipent_id and m.recipent = :sender_id')
                     ->orderBy('m.created_at', 'ASC')
                     ->setParameter('sender_id', $sender_id)
                     ->setParameter('recipent_id', $recipent_id);
        $query =  $queryBuilder->getQuery();
        $messages = $query->getResult();
        return $messages;
    }

